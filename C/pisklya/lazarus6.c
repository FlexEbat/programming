#include <stdio.h>   // Подключение библиотеки для ввода-вывода
#include <stdlib.h>  // Подключение библиотеки для работы с памятью и random
#include <time.h>    // Подключение библиотеки для работы со временем

int main() {  // Главная функция программы
    int matrix[10][10];     // Объявление двумерного массива 10x10
    int row_sums[10] = {0}; // Массив для хранения сумм строк, инициализированный нулями
    int col_sums[10] = {0}; // Массив для хранения сумм столбцов, инициализированный нулями
    
    srand(time(NULL));      // Инициализация генератора случайных чисел текущим временем
    
    // Первый двойной цикл: заполнение матрицы случайными числами
    for (int i = 0; i < 10; i++) {         // Внешний цикл по строкам
        for (int j = 0; j < 10; j++) {     // Внутренний цикл по столбцам
            matrix[i][j] = 10 + rand() % 90; // Генерация случайного числа от 10 до 99
        }
    }
    
    // Второй двойной цикл: вывод матрицы и подсчёт сумм
    printf("Matrix and row sums:\n\n");     // Заголовок перед выводом матрицы
    for (int i = 0; i < 10; i++) {         // Цикл по строкам
        for (int j = 0; j < 10; j++) {     // Цикл по столбцам
            printf("%3d ", matrix[i][j]);   // Вывод элемента матрицы с шириной 3 символа
            row_sums[i] += matrix[i][j];    // Добавление элемента к сумме строки
            col_sums[j] += matrix[i][j];    // Добавление элемента к сумме столбца
        }
        printf("   |  %d\n", row_sums[i]); // Вывод суммы строки после каждой строки
    }
    
    // Вывод разделительной линии перед суммами столбцов
    printf("\n     ");                      // Отступ перед линией
    for (int j = 0; j < 10; j++) {         // Цикл для вывода линии
        printf("---");                      // Вывод части разделительной линии
    }
    
    // Вывод сумм столбцов
    printf("\n     ");                      // Отступ перед суммами столбцов
    for (int j = 0; j < 10; j++) {         // Цикл по всем столбцам
        printf("%3d ", col_sums[j]);        // Вывод суммы столбца с шириной 3 символа
    }
    printf("\n");                           // Перевод строки в конце
    
    return 0;                               // Завершение программы с кодом 0 (успешно)
}